//Program to print pattern 9,19,29,39.......119
class seriesPattern{
    public static void main(String args[]){                          //main method
        int num=9;                                                   //initialize the number
        for(int i=1;i<=12;i++){                                      //for loop
            System.out.println(num);
            num +=10;

        }
    }
} 